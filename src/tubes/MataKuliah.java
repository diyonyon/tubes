public class MataKuliah {
    private String nama;
    private String kdMk;

    public MataKuliah(String nama, String kdMk) {
        this.nama = nama;
        this.kdMk = kdMk;
    }

    public String getKdMk() {
        return kdMk;
    }

    public String getNama() {
        return nama;
    }
    
}
